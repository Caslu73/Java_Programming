package day04_concatenation;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println( 100 + 200 ); // ADDITION because i have numbers on both sides

        System.out.println( 40 - 10 ); // Subtraction cause i have numbers both sides

        System.out.println(50 / 6);

        System.out.println(50 / 6.0);

        System.out.println(50.0 / 6);

        System.out.println(50d / 6); // Adding a d it convert the integer number to a double

        System.out.println(   (int) (2.5 / 0.5)  );

        //System.out.println(9 / 0); Error cause trying to divide by zero

        System.out.println(10 * 2);

        System.out.println( 20 * 12);

        System.out.println( 100 % 13 );

        System.out.println(45 % 8);



    }
}
